//获取Cookie
function getCookie(name){
  var re = new RegExp(name + "=([^;]+)");
  var value = re.exec(document.cookie);
  return (value != null) ? unescape(value[1]) : null;
}

//验证是否登陆
function isLogin() {
	var account = $.cookie('gymAccount');
	if(!account){
		account = "请登录";
	}
	$("#userName").text(account);
}

//重新登陆
function relogin(){
	setTimeout(function(){
		window.top.location.replace('index.html?redirect=' + encodeURIComponent(location.href));
	}, 1000);
}

/**
 * 处理服务器响应结果
 * @param data json type
 * @returns {Boolean}
 */
function handleResponse(data){
    if (data.STATUS) {
        return true;
    } else {
    	if( data.ERROR=='No Login!'){
            alert('登陆超时，请重新登录!');
            relogin();
        }else{
            alert(data.INFO);
        }
    	return false;
    }
	
}

